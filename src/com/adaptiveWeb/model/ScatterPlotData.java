package com.adaptiveWeb.model;

public class ScatterPlotData {
	
	String username;
	int noOfInteractions;
	int noOfLogins;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getNoOfInteractions() {
		return noOfInteractions;
	}
	public void setNoOfInteractions(int noOfInteractions) {
		this.noOfInteractions = noOfInteractions;
	}
	public int getNoOfLogins() {
		return noOfLogins;
	}
	public void setNoOfLogins(int noOfLogins) {
		this.noOfLogins = noOfLogins;
	}
	

}
